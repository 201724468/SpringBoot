package egovframework.example.main.vo;

public class StudentVo {
	private int studentSeq;
    private int classSeq;
    private String studentName;
    private String studentId;
    private int studentYear;
    private int studentAge;
    private String studentPhoneNum;
	/**
	 * @return the studentSeq
	 */
	public int getStudentSeq() {
		return studentSeq;
	}
	/**
	 * @param studentSeq the studentSeq to set
	 */
	public void setStudentSeq(int studentSeq) {
		this.studentSeq = studentSeq;
	}
	/**
	 * @return the classSeq
	 */
	public int getClassSeq() {
		return classSeq;
	}
	/**
	 * @param classSeq the classSeq to set
	 */
	public void setClassSeq(int classSeq) {
		this.classSeq = classSeq;
	}
	/**
	 * @return the studentName
	 */
	public String getStudentName() {
		return studentName;
	}
	/**
	 * @param studentName the studentName to set
	 */
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	/**
	 * @return the studentId
	 */
	public String getStudentId() {
		return studentId;
	}
	/**
	 * @param studentId the studentId to set
	 */
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	/**
	 * @return the studentYear
	 */
	public int getStudentYear() {
		return studentYear;
	}
	/**
	 * @param studentYear the studentYear to set
	 */
	public void setStudentYear(int studentYear) {
		this.studentYear = studentYear;
	}
	/**
	 * @return the studentAge
	 */
	public int getStudentAge() {
		return studentAge;
	}
	/**
	 * @param studentAge the studentAge to set
	 */
	public void setStudentAge(int studentAge) {
		this.studentAge = studentAge;
	}
	/**
	 * @return the studentPhoneNum
	 */
	public String getStudentPhoneNum() {
		return studentPhoneNum;
	}
	/**
	 * @param studentPhoneNum the studentPhoneNum to set
	 */
	public void setStudentPhoneNum(String studentPhoneNum) {
		this.studentPhoneNum = studentPhoneNum;
	}
}
